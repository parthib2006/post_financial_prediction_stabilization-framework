# Recursive Forecast
def recursive_forecast(
    model,
    last_window,
    steps
):

    preds = []

    current = last_window.copy()

    for _ in range(steps):

        pred = model.predict(
            current.reshape(1,-1)
        )[0]

        preds.append(pred)

        current = np.roll(current, -1)

        current[-1] = pred

    return np.array(preds)

