# Financial ABSM Stabilizer
# ============================================

def absm_stabilizer(
    series,
    beta=0.95,
    sigma=0.0005,
    alpha=0.05
):

    stabilized = []

    x_prev = series[0]

    for x in series:

        # contraction mapping
        T_x = (
            (1 - alpha) * x_prev
            +
            alpha * x
        )

        epsilon = np.random.normal(
            0,
            sigma
        )

        x_new = (
            beta * T_x
            +
            (1 - beta) * x
            +
            epsilon
        )

        stabilized.append(x_new)

        x_prev = x_new

    return np.array(stabilized)

