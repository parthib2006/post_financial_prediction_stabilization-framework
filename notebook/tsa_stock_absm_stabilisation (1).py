# Evaluation Metrics
def evaluate(y_true, y_pred):

    rmse = np.sqrt(
        mean_squared_error(y_true, y_pred)
    )

    mae = mean_absolute_error(
        y_true,
        y_pred
    )

    error_var = np.var(
        y_true - y_pred
    )

    traj_var = np.var(y_pred)

    return rmse, mae, error_var, traj_var

# ============================================
# Directional Accuracy
# ============================================

def directional_accuracy(
    y_true,
    y_pred
):

    y_true = np.array(
        y_true
    ).flatten()

    y_pred = np.array(
        y_pred
    ).flatten()

    min_len = min(
        len(y_true),
        len(y_pred)
    )

    y_true = y_true[:min_len]
    y_pred = y_pred[:min_len]

    true_dir = np.sign(
        np.diff(y_true)
    )

    pred_dir = np.sign(
        np.diff(y_pred)
    )

    min_dir = min(
        len(true_dir),
        len(pred_dir)
    )

    return np.mean(
        true_dir[:min_dir]
        ==
        pred_dir[:min_dir]
    )

