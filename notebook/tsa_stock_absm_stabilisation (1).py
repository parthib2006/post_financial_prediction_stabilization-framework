def dm_test(
    y_true,
    pred1,
    pred2
):

    e1 = y_true - pred1
    e2 = y_true - pred2

    d = (e1**2) - (e2**2)

    mean_d = np.mean(d)
    var_d = np.var(d)

    dm_stat = (
        mean_d
        /
        np.sqrt(var_d / len(d))
    )

    p_value = 2 * (
        1 - norm.cdf(abs(dm_stat))
    )

    return dm_stat, p_value

