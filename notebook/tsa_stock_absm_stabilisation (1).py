from scipy.stats import ttest_rel

def paired_test(
    y_true,
    raw_pred,
    stab_pred,
    model_name
):

    min_len = min(
        len(y_true),
        len(raw_pred),
        len(stab_pred)
    )

    y_true = y_true[:min_len]

    raw_pred = raw_pred[:min_len]

    stab_pred = stab_pred[:min_len]

    raw_error = y_true - raw_pred

    stab_error = y_true - stab_pred

    stat, p = ttest_rel(
        raw_error,
        stab_error
    )

    print(
        f"{model_name} p-value = {p:.8f}"
    )

